package com.many;

public class Customer {

    private int cid;
    private String name;
    private int fkey;

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getFkey() {
        return fkey;
    }

    public void setFkey(int fkey) {
        this.fkey = fkey;
    }
}
